class TestEntryNode:
    def test_classifies_diagnostic_questions(self, base_state):
        state = {**base_state, "query": "Error 404 in the asset register"}
        result = entry_node(state)
        assert result["query_type"] == "DIAGNOSTIC"
        assert "ENTRY" in result["node_trace"]

    def test_classifies_diagnostic_questions_with_why(self, base_state):
        state = {**base_state, "query": "Why is the export rejected by the system?"}
        result = entry_node(state)
        assert result["query_type"] == "DIAGNOSTIC"

    def test_classifies_configuration_questions(self, base_state):
        state = {**base_state, "query": "Which fields are required to configure the workflow?"}
        result = entry_node(state)
        assert result["query_type"] == "CONFIGURATION"
        assert result["retrieval_strategy"] == "semantic"

    def test_classifies_prerequisites_as_configuration(self, base_state):
        state = {**base_state, "query": "What prerequisites must be completed before using the workflow?"}
        result = entry_node(state)
        assert result["query_type"] == "CONFIGURATION"

