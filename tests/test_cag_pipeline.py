class TestIngestion:
    def test_chunker_splits_long_documents(self):
        from langchain_core.documents import Document

        from cag.ingestion.chunker import chunk_documents

        long_text = "Test paragraph. " * 200
        docs = [Document(page_content=long_text, metadata={"filename": "test.txt", "source": "test"})]
        chunks = chunk_documents(docs)
        assert len(chunks) > 1

    def test_chunker_preserves_metadata(self):
        from langchain_core.documents import Document

        from cag.ingestion.chunker import chunk_documents

        docs = [Document(page_content="Short text.", metadata={"filename": "x.txt", "domain_module": "workflow"})]
        chunks = chunk_documents(docs)
        assert chunks[0].metadata["domain_module"] == "workflow"
        assert "chunk_index" in chunks[0].metadata

    def test_loader_extracts_domain_module(self):
        from cag.ingestion.loader import _extract_domain_module

        assert _extract_domain_module("manual_onboarding_workflow") == "onboarding"
        assert _extract_domain_module("guide_asset_register_policy") == "asset"
        assert _extract_domain_module("team_documentation") == "team"
